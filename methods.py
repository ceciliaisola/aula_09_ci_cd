def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sum (value1, value2):
    sum = value1 + value2
    return sum

def sub(value1,value2):
    sub = value1 - value2
    return sub

def mult(value1,value2):
    mult = value1 * value2
    return mult

def div(value1,value2):
    div = value1 / value2
    return div